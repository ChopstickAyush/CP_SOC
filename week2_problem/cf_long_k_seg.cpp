void solve(){
    int n;
    cin>>n;
    int k;
    cin >>k;
    queue<int> q1;
    unordered_map<int,int> x;
    set<int> unique;
    int aleft=1,aright=1;
    int left=1,right=1;
    loop0(i,n)
    {
        int right=i+1;
        int no;
        cin >> no;
        if (unique.count(no)){
            x[no]++;
            q1.push(no);
        }
        else{
            while(unique.size()==k){
                    int z=q1.front();
                    x[z]--;
                    if (x[z]==0){
                        unique.erase(z);
                    }
                    q1.pop();
            }
            x[no]++;
            q1.push(no);
            unique.insert(no);
        }
        int left=right+1-q1.size();
        if (aright-aleft<right-left){
            aright=right;
            aleft=left;
        }
    }
    cout << aleft<< " "<<aright<<"\n";
}

